# Fundamental Data Types
print(2 + 4)
print(2 - 4)
print(2 *  4)
print(2 / 4)

# We can use the type() method to check the type
print(type(2 + 4))
print(type(2 / 4)) # 0.5, floating point number -> number with decimal point
print(2 ** 2) # This means power of, its exponent
print(2 // 4) # Returns an integer rounded down
print(5 % 4) # Returns the remainder of division

# Math functions -> functions are actions that we can perform on the data
print(round(3.1)) # Rounds down
print(round(3.9)) # Rounds up
print(abs(-20)) # Absolute value of -20
