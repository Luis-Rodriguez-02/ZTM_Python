# Logical operators

# > -> greater than
print(4 > 5)
# < -> less than
print(4 < 5)
# == -> equality
print(4 == 5)

# <= -> less than or equal to
print(5 <= 4)
# >= greater than or equal to
print(5 >= 4)
# != -> not equal to
print(5 != 4)

# not -> opposite
print(not(True))
# and -> both
print(True and True)
# or -> either
print(True or False)
