# looping

for letters in 'Zero to Mastery':  # 'Zero to Mastery is an iterable, letters is a variable
    print(letters)

for i in [1, 2, 3, 4, 5]:
    print(i)

for even in {2, 4, 6, 8}:
    print(even)

for odd in (2, 3, 5, 7, 9):
    print(odd)

for item in [1, 2, 3, 4, 5]:
    for x in ['a', 'b', 'c']:
        print(item, x)
